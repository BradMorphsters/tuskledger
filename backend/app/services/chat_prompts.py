"""
Curated, pre-built prompts for the in-app "Ask" panel.

Why a fixed catalog instead of free-form chat:

  * The local LLM target is llama3.1:8b. An 8B model is fine at
    rephrasing structured numbers but it's not reliable at picking the
    right SQL/aggregation when given an arbitrary natural-language
    question. By limiting the surface to a handful of curated prompts
    we get to do the data lookup in Python (where we have tests) and
    leave the model with the much narrower job of writing prose around
    pre-computed numbers.

  * The "no hallucination" invariant from the AI Insights card carries
    over: every dollar figure in the response was computed from the DB
    in a Python handler, NOT generated by the model. The model can
    only restate numbers it was given.

Architecture mirrors `insights_narrative.py`:

  1. A handler builds a JSON "bundle" of facts for a given prompt + horizon.
  2. `build_user_prompt(prompt_id, bundle)` wraps the bundle in a
     handler-specific instruction.
  3. The router calls the OllamaClient with CHAT_SYSTEM_PROMPT + the
     user prompt and returns the model's text.

Adding a new curated prompt:
  1. Add an entry to `PROMPT_CATALOG`.
  2. Implement a `_build_<id>_bundle(db, horizon)` function below.
  3. Register the handler in `_HANDLERS`.

Demo mode: handlers run against the demo DB transparently (the router
passes the right session via get_db). Numbers will be the synthetic
Alex Carter values from seed_demo.py — that's the point.
"""
