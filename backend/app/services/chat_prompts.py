# ─── Shared system prompt ──────────────────────────────────────────────
#
# Same shape as insights_narrative.SYSTEM_PROMPT but tuned for
# single-question conversational answers (one short paragraph rather
# than 2-3 paragraphs). Hard rules are non-negotiable: the model only
# restates numbers it was given.
CHAT_SYSTEM_PROMPT = """You are a concise financial assistant answering
a single question in the user's personal finance dashboard. You will be
given a JSON object of pre-computed facts.

Hard rules — these are not suggestions:
  - ONLY use numbers that appear verbatim in the provided JSON. Do not
    estimate, extrapolate, or invent any dollar figures, percentages,
    counts, or dates.
  - Do not give financial advice. Do not warn about budgeting. Do not
    recommend categories to cut, accounts to open, or trades to make.
    Just answer the question with the facts you were given.
  - 1 to 3 short sentences. Plain English, no bullet lists, no
    markdown, no headings, no sign-off.
  - Refer to the user as "you".
  - The JSON's "horizon_label" tells you what time window the numbers
    cover. Use that label naturally in the answer (e.g. "over the past
    week", "today").
  - If the JSON contains a `comparison` field, mention the change vs
    that comparison ONCE, briefly. If `comparison` is null, don't
    speculate about trends.
  - If the JSON has no meaningful data (e.g. empty list, zero total,
    "no_data": true), say so plainly in one sentence — don't pad.

Answer the user's question directly. Open with the headline number.
"""


# ─── Catalog ───────────────────────────────────────────────────────────
#
# What the frontend gets from GET /api/chat/prompts. Stable shape; the
# UI uses `id` to POST back to /api/chat/answer.
#
# Horizon ids are strings (not enums) because the frontend renders them
# as chips; whatever the user picks comes back to the handler as-is.

@dataclass
class PromptHorizon:
    id: str            # "1d" | "1w" | "1mo" | "1yr" | "today" | "7d" | "30d"
    label: str         # short label shown in the UI chip

    def to_dict(self) -> dict:
        return {"id": self.id, "label": self.label}


@dataclass
class PromptDef:
    id: str
    label: str          # the question itself, e.g. "How much have I spent"
    description: str    # subtle one-liner shown under the question chip
    horizons: list[PromptHorizon]

    def to_dict(self) -> dict:
        return {
            "id": self.id,
            "label": self.label,
            "description": self.description,
            "horizons": [h.to_dict() for h in self.horizons],
        }


